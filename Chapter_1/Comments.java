/**
 * This program demonstrates the three types of comments in Java.
 */
public class Comments {
    public static void main(String[] args) {
        
        // Single-line comment
        System.out.println("Demonstrating three types of comments in Java.");

        /*
         Multi-line comment
         Useful for explaining blocks of code.
        */
        System.out.println("Multi-line comments can span multiple lines.");

        /**
         * Documentation comment
         * Commonly used for generating documentation.
         */
        System.out.println("Documentation comments are used for Javadoc.");
    }
}


